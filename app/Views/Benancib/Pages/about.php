<h1>Sobre a Benancib</h1>
<p class="about">Desde 1999, a prof. Lídia Silva de Freitas da Universidade Federal Fluminense (UFF) trabalha com dados
    de análise
    quali-quantitativa sobre a produção intelectual nacional e internacional da Ciência da Informação, por meio do
    projeto
    Questões em Rede. A proposta foi analisar temático-discursivamente e através das relações de autoridade as diversas
    configurações históricas dos Grupos de Trabalho dos ENANCIBs, desde sua criação, em 1994. Para tanto, ela e os
    membros
    de grupo de pesquisa (Informação, Discurso e Memória), com destaque para a aluna Lídia Martini Coelho Brandão Salek,
    elaboraram uma base de dados em DSPACE a fim de sistematizar o conteúdo do evento. Tal recurso, apoiado pela FAPERJ,
    pela UFF, e pelo CNPq na forma de bolsas PIBIC desde 2009 e desde 2013 Bolsa de Produtividade, foi intitulado de
    BENANCIB, cobrindo os trabalhos apresentados nos Enancib e lançado de forma beta 2012.</p>
<p class=" about">
    O projeto teve em sua gênese a parceria da ANCIB e foi viabilizado por financiamento da Fundação Carlos Chagas de
    Amparo à Pesquisa do Rio de Janeiro (FAPERJ), e também com bolsas do CNPq e apoio técnico da Superintendência de
    Tecnologia da Informação – STI/ UFF, e pode ficar disponível à comunidade científica via internet. A coleta se
    encerra e entre 2014 e 2016 a base de dados deixa de ser alimentada. Em 2016, é constituída uma comissão para
    gerenciar a base e atualizar seus dados, ligada ao Departamento de Ciência da Informação (GCI/UFF). Seus
    integrantes são, além da professora Lídia Silva de Freitas, as professoras Maria Luiza de Almeida Campos e
    Michely Jabala Mamede Vogel. Ao fim de 2017 a base de dados é atualizada com os anais de 2014, 2015 e 2016, com
    o auxílio de bolsistas PIBITI, PIBIC e alunos voluntários da UFF.</p>
<p class="about">Em 2018, a comissão ficou um tempo em
    suspensa: as professoras Lídia e Maria Luiza se aposentaram e a professora Michely estava em licença. Em 2019, a
    comissão é reformulada, composta por Michely e pelos professores Linair Maria Campos, Regina de Barros Cianconi
    e Marcia Jurkiewicz Bossy, porém a base de dados estava com problemas técnicos, oriundos da falta de atualização
    do DSPACE. Um bolsista é contratado com apoio da Pró-Reitoria de Pesquisa, Pós-Graduação e Inovação
    (PROPPI/UFF). É feita a atualização, mas na mesma época a STI/UFF, assim como toda universidade enfrenta
    diversos cortes de pessoal e verbas e o benancib permanece instável sem possibilidade de atualização. Chega a
    pandemia e a comissão passa a buscar soluções e ou parcerias para voltar a atualizar a base de dados.</p>
<p class="about">Em setembro de 2021 a gestão do Benancib passa do GCI para o Programa de Pós-graduação em Ciência da
    Informação - PPGCI/UFF, sendo seus membros: Profs. Michely (presidente), Regina, Linair e Daniel (suplente) e uma
    parceria com o Programa de Pós-Graduação em Ciência da Informação da Universidade Federal do Rio Grande do Sul
    (PPGCIN/UFRGS) começa a ser desenhada. A escolha da UFRGS foi estratégica: a universidade, na pessoa do prof. Rene
    Faustino Gabriel Junior administra a reconhecida base de dados BRAPCI. Finalmente, em julho de 2022 o convênio
    UFF/UFRGS é firmado e o BENANCIB passa a integrar a BRAPCI, tendo assim garantidos tanto sua atualização quanto
    acesso.</p>